def normalize_language(value: str | None) -> str:
    if value in SUPPORTED_LANGUAGES:
        return value
    return DEFAULT_LANGUAGE


def tr(language: str, key: str, **kwargs: object) -> str:
    language = normalize_language(language)
    template = TRANSLATIONS.get(language, {}).get(key) or TRANSLATIONS[DEFAULT_LANGUAGE].get(key) or key
    return template.format(**kwargs)


def status_text(language: str, value: str | None) -> str:
    language = normalize_language(language)
    normalized = (value or "unknown").lower()
    return STATUS_LABELS.get(language, {}).get(normalized) or STATUS_LABELS[DEFAULT_LANGUAGE].get(normalized) or normalized
